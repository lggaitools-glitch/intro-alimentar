export function getFoodBySlug(slug: string): FoodEntry | undefined {
  return foodDatabase.find((f) => f.slug === slug);
}

export function getFoodsByCategory(category: FoodCategory): FoodEntry[] {
  return foodDatabase.filter((f) => f.category === category);
}

export function getAllCategories(): FoodCategory[] {
  return [...new Set(foodDatabase.map((f) => f.category))];
}
