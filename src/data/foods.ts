export function getFoodsByMonth(month: number): Food[] {
  return foods.filter(f => f.monthFrom <= month && (f.monthTo === undefined || f.monthTo >= month));
}

export function getFoodsByCategory(category: FoodCategory): Food[] {
  return foods.filter(f => f.category === category);
}

export function getIronRichFoods(): Food[] {
  return foods.filter(f => f.ironRich);
}
